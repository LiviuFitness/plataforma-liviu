-- ------------------------------------------------------------
-- Exclusiones del cliente
-- ------------------------------------------------------------
create table public.alimentos_excluidos (
  id uuid primary key default gen_random_uuid(),
  cliente_id uuid not null references public.profiles (id) on delete cascade,
  alimento_id uuid not null references public.alimentos (id) on delete cascade,
  creado_en timestamptz not null default now(),
  unique (cliente_id, alimento_id)
);

create index idx_alimentos_excluidos_cliente on public.alimentos_excluidos (cliente_id);

alter table public.alimentos_excluidos enable row level security;

create policy "entrenador todo" on public.alimentos_excluidos for all
  using (es_entrenador()) with check (es_entrenador());

create policy "cliente gestiona sus exclusiones" on public.alimentos_excluidos for all
  using (cliente_id = auth.uid()) with check (cliente_id = auth.uid());
