-- ============================================================
-- PLATAFORMA LIVIU — Migración 15: categorías de alimentos y
-- exclusiones del cliente (base para la futura generación
-- automática de dietas por macros).
--  · categoria en alimentos: proteina/carbohidrato/grasa/verdura/
--    fruta/lacteo/legumbre/otro. Se categoriza automáticamente la
--    base de 402 alimentos de Liviu; los que cree el propio
--    entrenador quedan sin categoría hasta que la asigne.
--  · alimentos_excluidos: alimentos que un cliente no quiere ver
--    en su dieta (alergias, cosas que no le gustan). El cliente
--    gestiona los suyos; el entrenador ve/gestiona los de
--    cualquier cliente.
-- ============================================================

