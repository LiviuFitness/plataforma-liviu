-- ============================================================
-- PLATAFORMA LIVIU — Migración 7: nutrición estructurada
--  · Base de alimentos del Excel de Liviu (con fibra y equivalencias)
--  · Comidas por alimento + gramos (Fase 3)
-- ============================================================

create table if not exists public.alimentos (
  id uuid primary key default gen_random_uuid(),
  nombre text not null,
  kcal_100 numeric(7,2) not null,
  prot_100 numeric(6,2) not null default 0,
  carb_100 numeric(6,2) not null default 0,
  gras_100 numeric(6,2) not null default 0,
  fibra_100 numeric(6,2),
  creado_por uuid references public.profiles (id) on delete set null
);

-- Equivalencias: 100 g del alimento ≈ X g de la alternativa
create table if not exists public.alimento_alternativas (
  id uuid primary key default gen_random_uuid(),
  alimento_id uuid not null references public.alimentos (id) on delete cascade,
  nombre text not null,
  gramos numeric(7,2) not null,
  orden integer not null default 0
);

-- Alimentos estructurados dentro de cada comida de la dieta
create table if not exists public.dieta_comida_alimentos (
  id uuid primary key default gen_random_uuid(),
  comida_id uuid not null references public.dieta_comidas (id) on delete cascade,
  alimento_id uuid not null references public.alimentos (id),
  gramos numeric(7,2) not null default 100,
  orden integer not null default 0
);

create index if not exists idx_alt_alimento on public.alimento_alternativas (alimento_id);
create index if not exists idx_dca_comida on public.dieta_comida_alimentos (comida_id);

alter table public.alimentos enable row level security;
alter table public.alimento_alternativas enable row level security;
alter table public.dieta_comida_alimentos enable row level security;

drop policy if exists "entrenador todo" on public.alimentos;
create policy "entrenador todo" on public.alimentos for all
  using (es_entrenador()) with check (es_entrenador());
drop policy if exists "lectura autenticados" on public.alimentos;
create policy "lectura autenticados" on public.alimentos for select
  using (auth.uid() is not null);

drop policy if exists "entrenador todo" on public.alimento_alternativas;
create policy "entrenador todo" on public.alimento_alternativas for all
  using (es_entrenador()) with check (es_entrenador());
drop policy if exists "lectura autenticados" on public.alimento_alternativas;
create policy "lectura autenticados" on public.alimento_alternativas for select
  using (auth.uid() is not null);

drop policy if exists "entrenador todo" on public.dieta_comida_alimentos;
create policy "entrenador todo" on public.dieta_comida_alimentos for all
  using (es_entrenador()) with check (es_entrenador());
drop policy if exists "cliente lee sus alimentos de dieta" on public.dieta_comida_alimentos;
create policy "cliente lee sus alimentos de dieta" on public.dieta_comida_alimentos for select
  using (exists (
    select 1 from public.dieta_comidas dc
    join public.dietas d on d.id = dc.dieta_id
    where dc.id = comida_id and d.cliente_id = auth.uid()
  ));

